//typeof (especifica quando o tipo de dados)

let texto = "abc";
let numero = 1;
console.log(typeof(texto));
console.log(typeof(numero));

//parseInt (converte uma string em número)

let texto1 = "5"
console.log(parseInt(texto1));

//number (converte string em numero, as string deve possui somente números)

let texto2 = "6";
console.log(Number(texto2));

//string ()

let numero1 = 111;
let texto3 = String(numero1);
console.log(typeof(texto3));

//toString

let numero2 = 200;
let texto4 = numero2.toString();
