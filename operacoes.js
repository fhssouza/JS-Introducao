// adição
console.log(2+2);

//subtração
console.log(10-5);

//multiplicação
console.log(10*5);

//divisão
console.log(10/2);

//incremento
let numero = 10;
console.log(numero++);
console.log(numero);

//decremento
console.log(--numero);

//comparação de maior
console.log(10 > 5);

//comparação de menor
console.log(10 < 5);

//comparação de menor ou igual
console.log(3 <= 3);

//comparação de maior ou igual
console.log(20 >= 20);

//comparação de igualdade
console.log(10 == 10);

//comparação de igualde rigido
console.log(5 === "5");

//comparação de diferente
console.log(10 != 10);

//comparação de diferente rigida
console.log(10 !== "10");

//comparador logico && (and)
console.log(10 > 5 && 8 < 10);

//comparador lógico || (or)
console.log(10 > 5 || 8 > 10);

//comparador de negação
console.log(!true);

//concatenar
let nome = "joão";
let sobrenome = "oliveira";
console.log(nome + " " + sobrenome);









