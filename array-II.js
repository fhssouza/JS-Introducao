let pessoa = ["vinicius", 1.80, "vinicius"];

console.log(pessoa);

//push (adicionar um ou mais elementos ao final do array)
pessoa.push(22);
console.log(pessoa);

//pop (elimina o último elemento de um array)
pessoa.pop();
console.log(pessoa);

//let exluido = pessoa.pop();
//console.log(exluido);

//unshit (adicionar um ou mais elementos ao inicio de um array)
pessoa. unshift(22);
console.log(pessoa);

//shift (elimina o primeiro elemento de uma array)
pessoa.shift();
console.log(pessoa);

//indexof (procura no array pelo elemento que recebe como parâmetro)
console.log(pessoa.indexOf(1.80));

//lastIndexOf (procura no array pelo elemento que recebe como parâmetro, excerto que começa a procura do elemento no final do array)
console.log(pessoa.lastIndexOf("vinicius"))

//join (junta os elementos de um array)
console.log(pessoa.join("-"));

//includes (também similar ao indexOf, mas retorna um booleano)
var frutas = ["maça", "pera", "morango"];
frutas.includes("morango");


